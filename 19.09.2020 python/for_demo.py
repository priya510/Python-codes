'''
    WAP to print first 10 natural numbers.
'''

#1st parameter - start value
#2nd parameter - stop  value
#3rd parameter - stride value/step value (bydefault it is +1)

for i in range(1,11):
    print(i)

print()
for i in range(1,11,1):
    print(i)

print()
for i in range(1,11,2):
    print(i)

print()
for i in range(10,0,-1):
    print(i)
    
print()
for i in range(10,0,-2):
    print(i)

#start value                 - 0
#stop  value                 - 10
#stride value/step value     - 1
    
print()
for i in range(10):
    print(i)












