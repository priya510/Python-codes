'''
    For Loop
'''

#First parameter  - start value
#Second parameter - stop value
#Third parameter  - stride value/step value bydefault (+1)

for i in range(1,11):
    print(i)

print()
for i in range(1,11,2):
    print(i)

print()
for i in range(10,0,-1):
    print(i)

print()
for i in range(10,0,-2):
    print(i)

#start value - 0
#last value  - 9
#step value  - 1 
print()
for i in range(10):
    print(i)


